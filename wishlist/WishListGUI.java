    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        int number = Integer.parseInt(this.idTF.getText());
        String description = this.descTF.getText();
        int price = Integer.parseInt(this.priceTF.getText());
        String url = this.urlTF.getText();
        
        Gift gift = null;
        
        if(cmbGift.getSelectedItem().equals("BoardGame"))
        {
            String bgName = JOptionPane.showInputDialog("Please Enter Board Game Name : ");
            String bgPublisher = JOptionPane.showInputDialog("Please Enter Publishers Name : ");
            gift = new BoardGame(number, description, price, url, bgName, bgPublisher);
        }
        
        else if(cmbGift.getSelectedItem().equals("CraftSupplies")) 
        {
            String csName = JOptionPane.showInputDialog("Please Enter Craft Supply Type/Name : ");
            String csPublisher = JOptionPane.showInputDialog("Please Enter Publishers Name : ");
            gift = new CraftSupplies(number, description, price, url, csName, csPublisher);
        }
        
        else if(cmbGift.getSelectedItem().equals("Clothing"))
        {
            String type = JOptionPane.showInputDialog("Please Enter Clothing Type : ");
            String size = JOptionPane.showInputDialog("Please Enter Clothing Size : ");
            String color = JOptionPane.showInputDialog("Please Enter Clothing Color : ");
            gift = new Clothing(number, description, price, url, type, size, color);
        }
        
        wishList.addGift(gift);
        loadGifts(); //adds gifts to listModel
        
        try {
            wishList.saveFile();
        } catch (IOException ex) {
            System.out.println("IO Exception - line 219");
        }
    }//GEN-LAST:event_addBTNActionPerformed
    
    private void lstValueChanged()
    {
        if(!listModel.isEmpty()) 
            
            /* 
            when selected aspects will change to match the things in list
            */
        {
            int index = lstGift.getSelectedIndex();
            Gift gift = wishList.getGift().get(index);
            
            //setting text 
            this.idTF.setText(Integer.toString(gift.getNumber()));
            this.descTF.setText(gift.getDescription());
            this.priceTF.setText(Integer.toString(gift.getPrice()));
            this.urlTF.setText(gift.getUrl());
            
            //type casting in combo box.
            if(gift instanceof BoardGame)
            {
                cmbGift.setSelectedItem("BoardGame");
            }
            
            else if(gift instanceof CraftSupplies)
            {
                cmbGift.setSelectedItem("CraftSupplies");
            }
            
            else if(gift instanceof Clothing)
            {
                cmbGift.setSelectedItem("Clothing");
            }
            
        }
        
    }
    
    
    private void loadGifts()
    
    {
        listModel.clear();
        
        for(int i = 0; i<wishList.getGift().size(); i++)
        
        {
            Gift gift = wishList.getGift().get(i); //add each gift
            
            listModel.addElement(gift);
        }
        lstGift.setModel(listModel); //setting the model
    }
    
