    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        int id = Integer.parseInt(JOptionPane.showInputDialog("Please Enter Gift Number : "));
        
        int index = wishList.searchGift(id);
        
        if(index!=-1) //iterate as long as valid
        {
            this.lstGift.setSelectedIndex(index);
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "NotFound");
        }
    }//GEN-LAST:event_searchBTNActionPerformed

